// 判断模块在当前模式下是否显示
export function isModuleVisible(moduleId, currentMode) {
  // 调试模式显示所有模块
  if (currentMode === 'debug') return true
  
  const allowedModes = moduleVisibility[moduleId] || []
  return allowedModes.includes(currentMode)
}
